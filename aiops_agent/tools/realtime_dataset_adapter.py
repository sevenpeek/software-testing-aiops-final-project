"""Adapt realtime Prometheus CSV into USAD/KPIRoot input files."""

from __future__ import annotations

import csv
import json
import shutil
from datetime import datetime
from pathlib import Path
from typing import Any


def _project_root() -> Path:
    return Path(__file__).resolve().parents[2]


def _resolve_path(project_root: Path, path_value: str | Path) -> Path:
    path = Path(path_value)
    if not path.is_absolute():
        path = project_root / path
    return path


def _read_rows(csv_path: Path) -> tuple[list[str], list[dict[str, str]]]:
    with csv_path.open("r", encoding="utf-8-sig", newline="") as file:
        reader = csv.DictReader(file)
        return list(reader.fieldnames or []), list(reader)


def _timestamp_to_epoch(value: str) -> str:
    try:
        return str(float(datetime.fromisoformat(value.replace("Z", "+00:00")).timestamp()))
    except Exception:
        return value


def _to_kpiroot_column(name: str) -> str:
    if name.endswith("__cpu"):
        return "cpu__" + name[: -len("__cpu")]
    if name.endswith("__memory"):
        return "memory__" + name[: -len("__memory")]
    return name


def build_realtime_datasets(
    config: dict[str, Any],
    prometheus_csv: str | Path,
    output_dir: str | Path | None = None,
    scenario_id: str = "realtime-paymentservice-cpu",
    alarm_name: str = "paymentservice",
) -> dict[str, Any]:
    project_root = _project_root()
    input_csv = _resolve_path(project_root, prometheus_csv)
    runtime_dir = output_dir or config.get("realtime_pipeline", {}).get("runtime_data_dir", "aiops_agent/runtime_data")
    output_path = _resolve_path(project_root, runtime_dir)
    output_path.mkdir(parents=True, exist_ok=True)
    timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")

    fieldnames, rows = _read_rows(input_csv)
    metric_columns = [name for name in fieldnames if name != "timestamp"]

    usad_input = output_path / f"usad_input_{timestamp}.csv"
    with usad_input.open("w", newline="", encoding="utf-8-sig") as file:
        writer = csv.DictWriter(file, fieldnames=["timestamp", "sample_index", "label", *metric_columns])
        writer.writeheader()
        for index, row in enumerate(rows):
            writer.writerow(
                {
                    "timestamp": row.get("timestamp", ""),
                    "sample_index": index,
                    "label": 0,
                    **{column: row.get(column, "") for column in metric_columns},
                }
            )

    kpiroot_columns = [_to_kpiroot_column(column) for column in metric_columns]
    alarm_source_column = "cpu__paymentservice" if "cpu__paymentservice" in kpiroot_columns else None
    if alarm_name and alarm_name not in kpiroot_columns:
        kpiroot_columns = [alarm_name, *kpiroot_columns]
    kpiroot_input = output_path / f"kpiroot_input_{timestamp}.csv"
    with kpiroot_input.open("w", newline="", encoding="utf-8-sig") as file:
        writer = csv.DictWriter(file, fieldnames=["timestamp", *kpiroot_columns])
        writer.writeheader()
        for row in rows:
            kpiroot_values = {_to_kpiroot_column(column): row.get(column, "") for column in metric_columns}
            if alarm_name and alarm_name not in kpiroot_values:
                kpiroot_values[alarm_name] = kpiroot_values.get(alarm_source_column or "", "")
            writer.writerow(
                {
                    "timestamp": _timestamp_to_epoch(row.get("timestamp", "")),
                    **kpiroot_values,
                }
            )

    phase2_scenario_dir = output_path / "kpiroot_phase2" / scenario_id
    processed_dir = phase2_scenario_dir / "processed"
    processed_dir.mkdir(parents=True, exist_ok=True)
    kpi_matrix = processed_dir / "kpi_matrix.csv"
    shutil.copy2(kpiroot_input, kpi_matrix)

    metadata = {
        "scenario": scenario_id,
        "alarm": alarm_name,
        "scenario_id": scenario_id,
        "system": config.get("system", {}).get("name", "Online Boutique"),
        "namespace": config.get("system", {}).get("namespace", "online-boutique"),
        "generated_from": "prometheus_realtime csv",
        "fault": {
            "tool": "realtime_pipeline",
            "kind": "observed_metrics",
            "target_app": "paymentservice",
            "expected_root_cause_kpis": ["cpu__paymentservice"],
        },
        "timing": {
            "export_start": rows[0].get("timestamp") if rows else "",
            "export_end": rows[-1].get("timestamp") if rows else "",
        },
        "data": {
            "processed_matrix": "processed/kpi_matrix.csv",
        },
        "notes": [
            "Generated by aiops_agent realtime_dataset_adapter.",
            "realtime dry-run compatible metadata",
            "Canonical format is generated because final online input contracts should be confirmed with USAD/KPIRoot project owners.",
        ],
    }
    metadata_yaml = phase2_scenario_dir / "metadata.yaml"
    metadata_yaml.write_text(_dict_to_simple_yaml(metadata), encoding="utf-8-sig")

    adapter_meta = output_path / f"realtime_dataset_{timestamp}.meta.json"
    result = {
        "prometheus_csv": str(input_csv),
        "usad_input_csv": str(usad_input),
        "kpiroot_input_csv": str(kpiroot_input),
        "kpiroot_phase2_dir": str(output_path / "kpiroot_phase2"),
        "kpiroot_scenario_dir": str(phase2_scenario_dir),
        "kpiroot_matrix_csv": str(kpi_matrix),
        "metadata_yaml": str(metadata_yaml),
        "metadata_json": str(adapter_meta),
        "warnings": [
            "USAD input is canonical CSV compatible with run_usad.py.",
            "KPIRoot input is adapted to phase2/<scenario>/processed/kpi_matrix.csv with generated metadata.yaml; scenario semantics should be confirmed by the KPIRoot project owners.",
        ],
    }
    adapter_meta.write_text(json.dumps(result, indent=2, ensure_ascii=False), encoding="utf-8-sig")
    return result


def _dict_to_simple_yaml(payload: dict[str, Any], indent: int = 0) -> str:
    lines: list[str] = []
    prefix = " " * indent
    for key, value in payload.items():
        if isinstance(value, dict):
            lines.append(f"{prefix}{key}:")
            lines.append(_dict_to_simple_yaml(value, indent + 2))
        elif isinstance(value, list):
            lines.append(f"{prefix}{key}:")
            for item in value:
                if isinstance(item, dict):
                    lines.append(f"{prefix}  -")
                    lines.append(_dict_to_simple_yaml(item, indent + 4))
                else:
                    lines.append(f"{prefix}  - {json.dumps(item, ensure_ascii=False)}")
        else:
            lines.append(f"{prefix}{key}: {json.dumps(value, ensure_ascii=False)}")
    return "\n".join(lines) + "\n"
